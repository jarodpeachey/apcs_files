public class GroceryItem {
  public String name;
  public double cost;
  public int id;
  public int quantity;

  public GroceryItem(String newName, double newCost, int newId, int newQuantity) {
    // Set Student variables
    name = newName;
    cost = newCost;
    id = newId;
    quantity = newQuantity;
  }
}
