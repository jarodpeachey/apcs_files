public class Hurricane {
  public int year;
  public String month;
  public int pressure;
  public double windSpeed;
  public String name;
  public int category;

  public Hurricane(int newYear, String newMonth, int newPressure, double newWindSpeed, String newName, int newCategory) {
    year = newYear;
    month = newMonth;
    pressure = newPressure;
    windSpeed = newWindSpeed;
    name = newName;
    category = newCategory;
  }
}
